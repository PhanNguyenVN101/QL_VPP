    public void laydulieuHD(ArrayList<HoaDon> dshd){
        tblData.removeAllElements();
        int j = 0;
        for(HoaDon i : dshd){
            Vector v = new Vector();
            v.add(j+1); v.add(i.getMahd()); 
            v.add(i.getTongsl()); v.add(i.getTongtienhd());
            v.add(i.getGiamgia()); v.add(i.getThanhtien()); 
            v.add(i.getTrangthai()); v.add(i.getTg());
            tblData.add(v);
            j++;
        }
        tbl_HoaDon.setModel(new DefaultTableModel(tblData, tblTitle));
    }
