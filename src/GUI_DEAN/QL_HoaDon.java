    public void CapNhatToanBoSL_SP(String mahd){
        ArrayList<String> dsmsp = CTHoaDonDAO.XuatMaSP_HD(mahd);
        for(String i:dsmsp){
            
            int soluongsp = CTHoaDonDAO.XuatSL_MaCTHD(mahd, i);
            
            String sql_CapNhatSL = "exec CapNhatSLSP_XoaHD '"+i+"',"+soluongsp+"";
            HoaDonDAO.ThemXoaSua(sql_CapNhatSL);
        }
    }
    private void btn_XoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_XoaActionPerformed
        // TODO add your handling code here:
        if(txt_MaHD.getText().equals(""))
            JOptionPane.showMessageDialog(this, "Mã hóa đơn không được để trống !","Thông báo",2);
        else
        {
            if(tbl_HoaDon.getSelectedRowCount()==1)
            {
                if(JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn xóa không?", "Thông báo", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION){
                    String mahd = txt_MaHD.getText().trim();
                    CapNhatToanBoSL_SP(mahd);
                    String sql = "delete CTHD where MaCTHD = '"+mahd+"'\n"+
                            "delete HoaDon where MaHD = '"+mahd+"'";
                    if(HoaDonDAO.ThemXoaSua(sql)!=-1)
                    {
                        clearAllTextField(this.getContentPane());
                        returnDefaultAllCbo(this.getContentPane());
                        dshd = HoaDonDAO.getDSHD();
                        laydulieuHD(dshd);
                        JOptionPane.showMessageDialog(this, "Xóa thành công !", "Thông báo", 1);
                    }
                    else
                        JOptionPane.showMessageDialog(this, "Dữ liệu này đang được sử dụng, không thể xóa", "Thông báo", 2);

                }
            }else{
                if(tbl_HoaDon.getRowCount()==0)
                    JOptionPane.showMessageDialog(this, "Bảng trống","Thông báo",2);
                else
                    JOptionPane.showMessageDialog(this, "Chưa chọn dòng cần xóa","Thông báo",2);
            }
        }
    }//GEN-LAST:event_btn_XoaActionPerformed

