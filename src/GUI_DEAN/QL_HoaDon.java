    private void tbl_HoaDonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_HoaDonMouseClicked
        // TODO add your handling code here:
        String STT = tbl_HoaDon.getValueAt(tbl_HoaDon.getSelectedRow(), 0).toString();
        String mahd = tbl_HoaDon.getValueAt(tbl_HoaDon.getSelectedRow(), 1).toString();
        String tongsl = tbl_HoaDon.getValueAt(tbl_HoaDon.getSelectedRow(), 2).toString();
        String tongtien = tbl_HoaDon.getValueAt(tbl_HoaDon.getSelectedRow(), 3).toString();
        String giamgia = tbl_HoaDon.getValueAt(tbl_HoaDon.getSelectedRow(), 4).toString();
        String thanhtien = tbl_HoaDon.getValueAt(tbl_HoaDon.getSelectedRow(), 5).toString();
        String trangthai = tbl_HoaDon.getValueAt(tbl_HoaDon.getSelectedRow(), 6).toString();
        String tg = "";
        if(tbl_HoaDon.getValueAt(tbl_HoaDon.getSelectedRow(), 7)!=null)
            tg = tbl_HoaDon.getValueAt(tbl_HoaDon.getSelectedRow(), 7).toString();
        else
            tg = "";
        String loaigg = HoaDonDAO.LoaiGG(mahd); 
        
        lbl_TG.setText(tg);
            
        lbl_STT.setText(STT);
        txt_MaHD.setText(mahd);
        txt_TongSL.setText(tongsl);
        txt_TongTien.setText(tongtien);
        txt_GiamGia.setText(giamgia);
        txt_ThanhTien.setText(thanhtien);
        
        if(trangthai.equals(cbo_TrangThai.getItemAt(0)))
            cbo_TrangThai.setSelectedIndex(0);
        else
            cbo_TrangThai.setSelectedIndex(1);
        
        if(loaigg.equals(cbo_LoaiGG.getItemAt(0)))
            cbo_LoaiGG.setSelectedIndex(0);
        else
            cbo_LoaiGG.setSelectedIndex(1);
            
    }//GEN-LAST:event_tbl_HoaDonMouseClicked

