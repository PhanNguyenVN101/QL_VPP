    public Boolean KiemTraTXT(){
        String hoten = txt_HoTen.getText().trim();
        String sdt = txt_SDT.getText().trim();
        String diachi = txtA_DiaChi.getText().trim();
        String manv = txt_MaNV.getText().trim();
        String socccd = txt_SoCCCD.getText().trim();
        String ngaysinh = txt_NgaySinh.getText().trim();
        String quequan = txt_QueQuan.getText().trim();
        String hinh = txt_Browser.getText().trim();
        
        
        if(manv.equals("") || diachi.equals("")|| socccd.equals("")|| ngaysinh.equals("")|| quequan.equals("") || sdt.equals("") || hoten.equals("") || hinh.equals("")){
            JOptionPane.showMessageDialog(this, "Thông tin nhân viên không được để trống !", "Thông báo", 2);
            return false;
        }
        
        if(manv.length()<=2)
        {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đúng mã nhân viên là NVxxx với x là số", "Thông báo", 2);
            return false;
        }
        
        String dauMaNV = manv.substring(0,2);
        if(dauMaNV.equals("NV") == false){
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đúng mã nhân viên là NVxxx với x là số", "Thông báo", 2);
            return false;
        }
        try {
            int duoimMaNV = Integer.parseInt(manv.substring(2));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đúng mã nhân viên là NVxxx với x là số", "Thông báo", 2);
            return false;
        }
        
        if(sdt.length()<10 || sdt.length()>10){
            JOptionPane.showMessageDialog(this, "Số điện thoại không hợp lệ !","Thông báo",2);
            return false;
        }
        String dauSDT = sdt.substring(0,1);
        if(!dauSDT.equals("0")){
            JOptionPane.showMessageDialog(this, "Số điện thoại không hợp lệ !","Thông báo",2);
            return false;
        }
        
        
        
        if(socccd.length()<12 || socccd.length()>12){
            JOptionPane.showMessageDialog(this, "Số CCCD không hợp lệ !","Thông báo",2);
            return false;
        }
        String dauCCCD = socccd.substring(0,1);
        if(!dauCCCD.equals("0")){
            JOptionPane.showMessageDialog(this, "Số CCCD không hợp lệ !","Thông báo",2);
            return false;
        }
        
        try{
            int dauNS = Integer.parseInt(ngaysinh.substring(0, 2));
            int cuoiNS = Integer.parseInt(ngaysinh.substring(6));
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Ngày sinh không hợp lệ !","Thông báo",2);
            return false;
        }
        ArrayList<String> aNgaySinh = new ArrayList<String>();
        String[] a = ngaysinh.split("/");
        for(String i:a)
            aNgaySinh.add(i);
        
        if(aNgaySinh.size() != 3 || aNgaySinh.get(0).length() !=2 || aNgaySinh.get(1).length() !=2 || aNgaySinh.get(2).length() !=4){
            JOptionPane.showMessageDialog(this, "Ngày sinh không hợp lệ !","Thông báo",2);
            return false;
        }
        
        
        return true;
    }
    public boolean KTSDTVaSCCCD(){
        String sdt = txt_SDT.getText().trim();
        String socccd = txt_SoCCCD.getText().trim();
        int ktsdt = NhanVienDAO.TimSDT(sdt);
        if(ktsdt==1)
        {
            JOptionPane.showMessageDialog(this, "Số điện thoại này đã có !","Thông báo",2);
            return false;
        }
        int ktsocccd = NhanVienDAO.TimSoCCCD(socccd);
        if(ktsocccd==1)
        {
            JOptionPane.showMessageDialog(this, "Số CCCD này đã có !","Thông báo",2);
            return false;
        }
        return true;
    }
    
