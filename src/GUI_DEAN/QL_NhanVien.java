    public void laydulieuNV(ArrayList<NhanVien> dsnv){
        tblData.removeAllElements();
        int j = 0;
        for(NhanVien i : dsnv){
            Vector v = new Vector();
            v.add(j+1); v.add(i.getMa()); 
            v.add(i.getHoten()); v.add(i.getGioitinh());
            v.add(i.getSdt()); v.add(i.getSocccd()); 
            v.add(i.getNgaysinh()); v.add(i.getDiachi()); 
            v.add(i.getQuequan());
            tblData.add(v);
            j++;
        }
        tbl_NhanVien.setModel(new DefaultTableModel(tblData, tblTitle));
    }
