    public Boolean KiemTraTXT(){
        String tentk = txt_TenTK.getText().trim();
        String matkhau = txt_MatKhau.getText().trim();
        
        
        if(tentk.equals("")){
            JOptionPane.showMessageDialog(this, "Tên tài khoản không được để trống !", "Thông báo", 2);
            return false;
        }
        
        if(matkhau.equals("")){
            JOptionPane.showMessageDialog(this, "Mật khẩu không được để trống !", "Thông báo", 2);
            return false;
        }
        
        
        return true;
    }
    
    private void btn_ThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ThemActionPerformed
        // TODO add your handling code here:
        if(KiemTraTXT()){
            if(JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn thêm không?", "Thông báo", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION){
                    String tentk = txt_TenTK.getText().trim();
                    String matkhau = txt_MatKhau.getText().trim();
                    String manv = txt_MaNV.getText().trim();
                    String quyen = cbo_Quyen.getSelectedItem().toString();
                    
                    String sql = "insert into TaiKhoan(TenTK,MaNV,Quyen,MatKhau)\n" +
                    "values\n" +
                    "('"+tentk+"','"+manv+"',N'"+quyen+"','"+matkhau+"')";
                    int kq=TaiKhoanDAO.ThemXoaSua(sql);
                    if(kq!=-1)
                    {
                        dstk = TaiKhoanDAO.getDSTK();
                        laydulieuTK(dstk);
                        JOptionPane.showMessageDialog(this, "Thêm thành công !", "Thông báo",1);
                    }
                    else
                         JOptionPane.showMessageDialog(this, "Tên tài khoản này đã tồn tại !", "Thông báo", 2);

                }
        }
    }//GEN-LAST:event_btn_ThemActionPerformed

    private void btn_XoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_XoaActionPerformed
        // TODO add your handling code here:
        if(txt_TenTK.getText().equals(""))
            JOptionPane.showMessageDialog(this, "Tên tài khoản không được để trống !","Thông báo",2);
        else
        {
            if(tbl_TaiKhoan.getSelectedRowCount()==1)
            {
                if(JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn xóa không?", "Thông báo", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION){
                    String tentk = txt_TenTK.getText().trim();
                    String quyen = TaiKhoanDAO.XuatQuyen(tentk);
                    String quyenql = tbl_TaiKhoan.getValueAt(tbl_TaiKhoan.getSelectedRow(), 4).toString();
                    if(quyen.equals(quyenql))
                        JOptionPane.showMessageDialog(this, "Quản lý không thể xóa quản lý", "Thông báo", 2);
                    else{
                        String sql = "delete from TaiKhoan where TenTK = '"+tentk+"'";
                        if(TaiKhoanDAO.ThemXoaSua(sql)!=-1)
                        {
                             dstk = TaiKhoanDAO.getDSTK();
                            laydulieuTK(dstk);
                            JOptionPane.showMessageDialog(this, "Xóa thành công !", "Thông báo", 1);
                        }
                        else
                            JOptionPane.showMessageDialog(this, "Dữ liệu này đang được sử dụng, không thể xóa", "Thông báo", 2);
                    }

                }
            }else{
                if(tbl_TaiKhoan.getRowCount()==0)
                    JOptionPane.showMessageDialog(this, "Bảng trống","Thông báo",2);
                else
                    JOptionPane.showMessageDialog(this, "Chưa chọn dòng cần xóa","Thông báo",2);
            }
        }
    }//GEN-LAST:event_btn_XoaActionPerformed

    private void btn_SuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SuaActionPerformed
        // TODO add your handling code here:
        if(KiemTraTXT()){
            if(tbl_TaiKhoan.getSelectedRowCount()==1)
            {
                if(JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn sửa không?", "Thông báo", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION){
                        String tentk = txt_TenTK.getText().trim();
                        String matkhau = txt_MatKhau.getText().trim();
                        String manv = txt_MaNV.getText().trim();
                        String quyen = cbo_Quyen.getSelectedItem().toString();
                        String sql = "update TaiKhoan \n" +
                        "set MatKhau='"+matkhau+"', MaNV='"+manv+"', Quyen=N'"+quyen+"'\n" +
                        "where TenTK = '"+tentk+"'";
                        TaiKhoanDAO.ThemXoaSua(sql);
                        dstk = TaiKhoanDAO.getDSTK();
                        laydulieuTK(dstk);
                        JOptionPane.showMessageDialog(this, "Sửa thành công !", "Thông báo", 1);
                }
            }else{
                if(tbl_TaiKhoan.getRowCount()==0)
                    JOptionPane.showMessageDialog(this, "Bảng trống","Thông báo",2);
                else
                    JOptionPane.showMessageDialog(this, "Chưa chọn dòng cần sửa","Thông báo",2);
            }
        }
    }//GEN-LAST:event_btn_SuaActionPerformed

