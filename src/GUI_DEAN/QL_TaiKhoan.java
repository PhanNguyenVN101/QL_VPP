    public void laydulieuNV(ArrayList<NhanVien> dsnv){
        tblDataNV.removeAllElements();
        int j = 0;
        for(NhanVien i : dsnv){
            Vector v = new Vector();
            v.add(j+1); v.add(i.getMa()); 
            v.add(i.getHoten()); v.add(i.getGioitinh());
            v.add(i.getSdt()); v.add(i.getSocccd()); 
            v.add(i.getNgaysinh()); v.add(i.getDiachi()); 
            v.add(i.getQuequan());
            tblDataNV.add(v);
            j++;
        }
        tbl_NhanVien.setModel(new DefaultTableModel(tblDataNV, tblTitleNV));
    }
    public void laydulieuTK(ArrayList<TaiKhoan> dstk){
        tblDataTK.removeAllElements();
        int j = 0;
        for(TaiKhoan i : dstk){
            Vector v = new Vector();
            v.add(j+1); v.add(i.getTentk()); 
            v.add(i.getMatkhau()); v.add(i.getManv());
            v.add(i.getQuyen());
            tblDataTK.add(v);
            j++;
        }
        tbl_TaiKhoan.setModel(new DefaultTableModel(tblDataTK, tblTitleTK));
    }
