    public Boolean KiemTraTXT(){
        String mancc = txt_MaNCC.getText().trim();
        String madon = txt_MaDon.getText().trim();
        
        if(mancc.equals("")){
            JOptionPane.showMessageDialog(this, "Mã nhà cung cấp không được để trống !", "Thông báo", 2);
            return false;
        }
        if(mancc.length()<=3)
        {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đúng mã nhà cung cấp là NCCxxx với x là số", "Thông báo", 2);
            return false;
        }
        
        String dauMaNCC = mancc.substring(0,3);
        if(dauMaNCC.equals("NCC") == false){
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đúng mã nhà cung cấp là NCCxxx với x là số", "Thông báo", 2);
            return false;
        }
        try {
            int duoimMaNCC = Integer.parseInt(mancc.substring(3));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đúng mã nhà cung cấp là NCCxxx với x là số", "Thông báo", 2);
            return false;
        }
        
        if(madon.equals("")){
            JOptionPane.showMessageDialog(this, "mã đơn không được để trống !", "Thông báo", 2);
            return false;
        }
        if(madon.length()<=3){
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đúng mã đơn là DDHxxx với x là số","Thông báo",2);
            return false;
        }
        String dauMaDDH = madon.substring(0,3);
        if(dauMaDDH.equals("DDH") == false){
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đúng mã đơn là DDHxxx với x là số", "Thông báo", 2);
            return false;
        }
        try {
            int duoiMaDDH = Integer.parseInt(madon.substring(3));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đúng mã đơn là DDHxxx với x là số", "Thông báo", 2);
            return false;
        }
        
        
        return true;
    }
