        txt_MaNCC.setText(mancc);
        txt_NhanVien.setText(nhanvien);
        txt_TongSLSPD.setText(TongSLSPD);
        String tg = "";
        if(tbl_DonDatHang.getValueAt(tbl_DonDatHang.getSelectedRow(), 4)!=null)
            tg = tbl_DonDatHang.getValueAt(tbl_DonDatHang.getSelectedRow(), 4).toString();
