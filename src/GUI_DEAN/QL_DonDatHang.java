    private void btn_DatHangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_DatHangActionPerformed
        // TODO add your handling code here:
        String madon = txt_MaDon.getText().trim();
        if(madon.equals("")){
            JOptionPane.showMessageDialog(this, "Mã đơn đặt hàng không được để trống !", "Thông báo", 2);
        }else{
            if(JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn đặt hàng không ?", "Thông báo", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION){

                int slsp = DonDatHangDAO.XuatTongSLSPDat(madon);
                if(slsp==0)
                    JOptionPane.showMessageDialog(this, "Chưa có sản phẩm trong đơn hàng !", "Thông báo",2);
                else{
                    String trangthaidh = DonDatHangDAO.XuatTTDatHang(madon);
                    if(trangthaidh.equals("Chưa đặt")){
                                String sql = "update DonDatHang set NgayDH = GETDATE() where MaDonDH = '"+madon+"'\n"+
                                             "update DonDatHang set TrangThaiDH = N'Đã đặt' where MaDonDH = '"+madon+"'";
                                int kq=DonDatHangDAO.ThemXoaSua(sql);
                                if(kq!=-1)
                                {
                                    dsddh = DonDatHangDAO.getDSDDH();
                                    laydulieuDDH(dsddh);
                                    JOptionPane.showMessageDialog(this, "Đặt hàng thành công !", "Thông báo",1);
                                    String ngaydh = DonDatHangDAO.XuatNgayDatHang(madon);
                                    lbl_NgayDatHang.setText(ngaydh);
                                }
                    }else
                        JOptionPane.showMessageDialog(this, "Đơn đặt hàng đã đặt rồi !", "Thông báo", 2);
                }
            }
        }
    }//GEN-LAST:event_btn_DatHangActionPerformed

