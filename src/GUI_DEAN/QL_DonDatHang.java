    public void laydulieuDDH(ArrayList<DonDatHang> dsddh){
        tblDataDonHang.removeAllElements();
        int j = 0;
        for(DonDatHang i : dsddh){
            Vector v = new Vector();
            v.add(j+1); v.add(i.getMaddh()); v.add(i.getMancc()); 
            v.add(i.getNhanvien()); v.add(i.getNgaydh());
            v.add(i.getTrangthaidh()); v.add(i.getCapnhattonkho()); v.add(i.getTongslspdh());
            tblDataDonHang.add(v);
            j++;
        }
        tbl_DonDatHang.setModel(new DefaultTableModel(tblDataDonHang, tblTitleDonHang));
    }
