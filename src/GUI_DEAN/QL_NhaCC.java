    public void laydulieuNCC(ArrayList<NhaCC> dsncc){
        tblData.removeAllElements();
        int j = 0;
        for(NhaCC i : dsncc){
            Vector v = new Vector();
            v.add(j+1); v.add(i.getMancc()); v.add(i.getSdtncc()); 
            v.add(i.getHotenncc()); v.add(i.getGioitinhncc());
            v.add(i.getDiachincc()); v.add(i.getSanphamcc());
            tblData.add(v);
            j++;
        }
        tbl_NhaCC.setModel(new DefaultTableModel(tblData, tblTitle));
    }
