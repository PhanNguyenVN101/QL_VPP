    public Boolean KiemTraTXT(){
        String mancc = txt_MaNCC.getText().trim();
        String hoten = txt_HoTen.getText().trim();
        String sdt = txt_SDT.getText().trim();
        String dc = txtA_DiaChi.getText().trim();
        String spcc = txtA_SPCC.getText().trim();
        
        if(mancc.equals("") || hoten.equals("") || sdt.equals("") || dc.equals("") || spcc.equals("")){
            JOptionPane.showMessageDialog(this, "Thông tin nhà cung cấp không được để trống !", "Thông báo", 2);
            return false;
        }
        if(mancc.length()<=2)
        {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đúng mã nhà cung cấp là NCCxxx với x là số", "Thông báo", 2);
            return false;
        }
        
        String dauMaNCC = mancc.substring(0,3);
        if(dauMaNCC.equals("NCC") == false){
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đúng mã nhà cung cấp là NCCxxx với x là số", "Thông báo", 2);
            return false;
        }
        try {
            int duoimMaNCC = Integer.parseInt(mancc.substring(3));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đúng mã nhà cung cấp là NCCxxx với x là số", "Thông báo", 2);
            return false;
        }
        
        if(sdt.length()<10 || sdt.length()>10){
            JOptionPane.showMessageDialog(this, "Số điện thoại không hợp lệ !","Thông báo",2);
            return false;
        }
        String dauSDT = sdt.substring(0,1);
        if(!dauSDT.equals("0")){
            JOptionPane.showMessageDialog(this, "Số điện thoại không hợp lệ !","Thông báo",2);
            return false;
        }
        
        return true;
    }
    public Boolean KTSDT(){
        String sdt = txt_SDT.getText().trim();
        int ktSDT = NhaCCDAO.TimKiemSDT(sdt);
        if(ktSDT==1){
            JOptionPane.showMessageDialog(this, "Số điện thoại này đã tồn tại !","Thông báo",2);
            return false;
        }
        return true;
    }
    private void btn_ThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ThemActionPerformed
        // TODO add your handling code here:
        if(KiemTraTXT() && KTSDT()){
            if(JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn thêm không ?", "Thông báo", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION){
                    String mancc = txt_MaNCC.getText().trim();
                    String hoten = txt_HoTen.getText().trim();
                    String gt = cbo_GT.getSelectedItem().toString();
                    String sdt = txt_SDT.getText().trim();
                    String dc = txtA_DiaChi.getText().trim();
                    String spcc = txtA_SPCC.getText().trim();
                    String sql = "insert into NhaCC(MaNhaCC,SoDTNCC,HoTenNCC,GioiTinhNCC,DiaChiNCC,SanPhamCC) values\n" +
                    "('"+mancc+"','"+sdt+"',N'"+hoten+"',N'"+gt+"',N'"+dc+"',N'"+spcc+"')";
                    int kq=NhaCCDAO.ThemXoaSua(sql);
                    if(kq!=-1)
                    {
                        dsncc = NhaCCDAO.getDSNCC();
                        laydulieuNCC(dsncc);
                        JOptionPane.showMessageDialog(this, "Thêm thành công !", "Thông báo",1);
                    }
                    else
                         JOptionPane.showMessageDialog(this, "Mã nhà cung cấp đã tồn tại !", "Thông báo", 2);

                }
        }
    }//GEN-LAST:event_btn_ThemActionPerformed

    private void btn_XoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_XoaActionPerformed
        // TODO add your handling code here:
        if(txt_MaNCC.getText().equals(""))
            JOptionPane.showMessageDialog(this, "Mã nhà cung cấp không được để trống !","Thông báo",2);
        else
        {
            if(tbl_NhaCC.getSelectedRowCount()==1)
            {
                if(JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn xóa không?", "Thông báo", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION){
                    String mancc = txt_MaNCC.getText().trim();
                    String sql = "delete from NhaCC where MaNhaCC = '"+mancc+"'";
                    if(NhaCCDAO.ThemXoaSua(sql)!=-1)
                    {
                        dsncc = NhaCCDAO.getDSNCC();
                        laydulieuNCC(dsncc);
                        JOptionPane.showMessageDialog(this, "Xóa thành công !", "Thông báo", 1);
                    }
                    else
                        JOptionPane.showMessageDialog(this, "Dữ liệu này đang được sử dụng, không thể xóa", "Thông báo", 2);

                }
            }else{
                if(tbl_NhaCC.getRowCount()==0)
                    JOptionPane.showMessageDialog(this, "Bảng trống","Thông báo",2);
                else
                    JOptionPane.showMessageDialog(this, "Chưa chọn dòng cần xóa","Thông báo",2);
            }
        }
    }//GEN-LAST:event_btn_XoaActionPerformed

