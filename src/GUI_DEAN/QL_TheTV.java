    public Boolean KiemTraTXT(){
        String makh = txt_MaKH.getText().trim();
        String mathe = txt_MaThe.getText().trim();
        
        if(makh.equals("")){
            JOptionPane.showMessageDialog(this, "Mã khách không được để trống !", "Thông báo", 2);
            return false;
        }
        if(makh.length()<=2)
        {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đúng mã khách là KHxxx với x là số", "Thông báo", 2);
            return false;
        }
        
        String dauMaKH = makh.substring(0,2);
        if(dauMaKH.equals("KH") == false){
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đúng mã khách là KHxxx với x là số", "Thông báo", 2);
            return false;
        }
        try {
            int duoimMaKH = Integer.parseInt(makh.substring(2));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đúng mã khách là KHxxx với x là số", "Thông báo", 2);
            return false;
        }
        
        if(mathe.equals("")){
            JOptionPane.showMessageDialog(this, "Mã thẻ không được để trống !", "Thông báo", 2);
            return false;
        }
        if(mathe.length()<=1)
        {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đúng mã thẻ là Txxx với x là số", "Thông báo", 2);
            return false;
        }
        
        String dauMaThe = mathe.substring(0,1);
        if(dauMaThe.equals("T") == false){
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đúng mã thẻ là Txxx với x là số", "Thông báo", 2);
            return false;
        }
        try {
            int duoimMaThe = Integer.parseInt(mathe.substring(1));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đúng mã thẻ là Txxx với x là số", "Thông báo", 2);
            return false;
        }
        
        int KTKhachHang = KhachHangDAO.TimKiemMaKH(makh);
        if(KTKhachHang==0){
            JOptionPane.showMessageDialog(this, "Chưa có thông tin khách hàng này !", "Thông báo", 2);
            return false;
        }
        
        return true;
    }
    private void btn_ThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ThemActionPerformed
        // TODO add your handling code here:
        if(KiemTraTXT()){
            if(JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn thêm không ?", "Thông báo", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION){
                    String makh = txt_MaKH.getText().trim();
                    String mathe = txt_MaThe.getText().trim();

                    String sql = "insert into TheTV(MaThe,MaKH) values('"+mathe+"','"+makh+"')";
                    int kq=TheTVDAO.ThemXoaSua(sql);
                    if(kq!=-1)
                    {
                        dst = TheTVDAO.getDST();
                        laydulieuTheTV(dst);
                        JOptionPane.showMessageDialog(this, "Thêm thành công !", "Thông báo",1);
                    }
                    else
                         JOptionPane.showMessageDialog(this, "Thẻ này đã tồn tại !", "Thông báo", 2);

                }
        }
    }//GEN-LAST:event_btn_ThemActionPerformed

    private void btn_XoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_XoaActionPerformed
        // TODO add your handling code here:
        if(txt_MaThe.getText().equals(""))
            JOptionPane.showMessageDialog(this, "Mã thẻ không được để trống !","Thông báo",2);
        else
        {
            if(tbl_TheTV.getSelectedRowCount()==1)
            {
                if(JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn xóa không?", "Thông báo", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION){
                    String mathe = txt_MaThe.getText().trim();
                    String sql = "delete from TheTV where MaThe = '"+mathe+"'";
                    if(TheTVDAO.ThemXoaSua(sql)!=-1)
                    {
                         dst = TheTVDAO.getDST();
                        laydulieuTheTV(dst);
                        JOptionPane.showMessageDialog(this, "Xóa thành công !", "Thông báo", 1);
                    }
                    else
                        JOptionPane.showMessageDialog(this, "Dữ liệu này đang được sử dụng, không thể xóa", "Thông báo", 2);

                }
            }else{
                if(tbl_TheTV.getRowCount()==0)
                    JOptionPane.showMessageDialog(this, "Bảng trống","Thông báo",2);
                else
                    JOptionPane.showMessageDialog(this, "Chưa chọn dòng cần xóa","Thông báo",2);
            }
        }
    }//GEN-LAST:event_btn_XoaActionPerformed

