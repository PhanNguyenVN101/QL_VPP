    public void laydulieuTheTV(ArrayList<TheTV> dst){
        tblData.removeAllElements();
        int j = 0;
        for(TheTV i : dst){
            Vector v = new Vector();
            v.add(j+1); v.add(i.getMathe()); v.add(i.getMakh()); 
            v.add(i.getTichdiem()); v.add(i.getNgaylapthe());
            tblData.add(v);
            j++;
        }
        tbl_TheTV.setModel(new DefaultTableModel(tblData, tblTitle));
    }
